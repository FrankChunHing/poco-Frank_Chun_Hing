document.getElementById('hamburger').addEventListener('click', function() {
    document.querySelector('nav').classList.toggle("new_nav");
    document.querySelector('.hamburger').classList.toggle("none");
    document.querySelector('.cross').classList.toggle("new_cross");
});

    
document.getElementById('cross').addEventListener('click', function() {
    document.querySelector('nav').classList.toggle("new_nav");
    document.querySelector('.hamburger').classList.toggle("none");
    document.querySelector('.cross').classList.toggle("new_cross");
});